import {
  buildPouXml,
  buildDataTypeXml,
  buildGvlXml,
  buildGvlStCode,
  type VarDeclaration,
  type PouDefinition,
  type DataTypeDefinition,
  type GvlDefinition,
} from '../utils/xml-builder.js';
import { validatePath, ensureDir } from '../utils/path-validation.js';
import { successResponse, errorResponse, getErrorMessage } from '../utils/mcp-helpers.js';
